
for a in range(51):
    print(a)
    # ^ Prints 0 to 50 in default increments of 1, an extra 1 to compensate.

print("--------------------------------------------------")
   
for b in range(50, -1, -1):
    print(b)
    # ^ Prints 50 to 0 in minus increment of 1, a -1 from 0 to compensate.

print("--------------------------------------------------")
    
for c in range(30, 51):
    print(c)
    # ^ Prints from 30 to 50 in increments of 1, extra 1 to compensate.

print("--------------------------------------------------")   

for d in range(50, 9, -2):
    print(d)
    # ^ Prints 50 to 10 in negative increments of 2, -1 from 10 to compensate.
    
print("--------------------------------------------------")

for e in range(100, 201, 5):
    print(e)
    # ^ Prints 100 to 200 in increments of 5, extra 1 to compensate.
    
print("--------------------------------------------------")
# ^ These prints are to divide each other in the console to make it look clearer.
